#include <iostream>
using namespace std;
int main()
{	
	int num=0;
   for(int i=1;i<=4;i++){
   		for(int j=1;j<=4;j++){
   			for(int k=1;k<=4;k++){
   				for(int l=1;l<=4;l++){
   					cout<<i*1000+j*100+k*10+l<<" ";
   					if(i!=j&&i!=k&&i!=l&&j!=k&&j!=l&&k!=l){
   							num++;
					   }
   				}
   			}
		}
   }
   cout<<endl;
	cout<<num<<endl;
    return 0;

}

